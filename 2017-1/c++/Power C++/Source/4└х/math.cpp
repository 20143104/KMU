#include <iostream>
#include <cmath>
using namespace std;

int main()
{
	double pi = 3.1415926535;

	cout << "sin()=" << sin(pi/2.0) << endl;
	cout << "cos()=" << cos(pi/2.0) << endl;
	cout << "tan()=" << tan(0.5) << endl;
	cout << "log()=" << log(10.0) << endl;
	cout << "log10()=" << log10(100.0) << endl;
	cout << "exp()=" << exp(10.0) << endl;
	return 0;
}
